    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            classes.MoeinAccounts ac = new classes.MoeinAccounts();
            ac.deleteAccount(id);
            // remove selected row from the model
            model.removeRow(jTable1.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(null, " یک سطر انتخاب کنید");
        }
        jTextField2.setText("");
        jTextField1.setText("");       }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            classes.MoeinAccounts ac = new classes.MoeinAccounts();
            int newid = Integer.parseInt(jTextField1.getText());
            name = jTextField2.getText();
            mahiat = jComboBox2.getSelectedItem().toString();
            totalId = Integer.parseInt(jTextField5.getText());
            ac.updateAccount(id, name, mahiat, totalId, newid);
            showData();
            jTextField2.setText("");
            jTextField1.setText("");
        } else {
            JOptionPane.showMessageDialog(null, " یک سطر انتخاب کنید");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if ((jTextField2.getText().equals("")) || (jTextField1.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, " فیلد ها را پر کنید");
        } else {
            classes.MoeinAccounts ac = new classes.MoeinAccounts();
            id = Integer.parseInt(jTextField1.getText());
            name = jTextField2.getText();
            mahiat = jComboBox2.getSelectedItem().toString();
            totalId = Integer.parseInt(jTextField5.getText());
            ac.saveAccount(id, name, mahiat, totalId);
            showData();
            jTextField2.setText("");
            jTextField1.setText("");

        }          }//GEN-LAST:event_btnAddActionPerformed

